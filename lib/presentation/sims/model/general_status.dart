class GeneralStatus {
  String academicYear;
  String semester;
  String batch;
  String regDate;
  String regCondition;
  String sGpa;
  double cGpa;
  double prevStatus;
  double finalStatus;

  GeneralStatus({
    required this.academicYear,
    required this.semester,
    required this.batch,
    required this.regDate,
    required this.regCondition,
    required this.sGpa,
    required this.cGpa,
    required this.prevStatus,
    required this.finalStatus,
  });
}
