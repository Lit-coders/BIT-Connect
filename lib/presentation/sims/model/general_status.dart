class GeneralStatus {
  String academicYear;
  String semester;
  String batch;
  String regDate;
  String regCondition;
  double sGpa;
  double cGpa;
  String prevStatus;
  String finalStatus;
  String fullName;

  GeneralStatus({
    required this.academicYear,
    required this.semester,
    required this.batch,
    required this.regDate,
    required this.regCondition,
    required this.sGpa,
    required this.cGpa,
    required this.prevStatus,
    required this.finalStatus,
    required this.fullName,
  });
}
